class ExchangeTorchField(FieldTerm):
    def __init__(self):
        self._state = None

    def h(self, state):
        if self._state is None:
            self._state = state
            # self._h = state._zeros(state.m.shape)
            self._h = VectorFunction(state)

            # TODO
            shape = state.mesh.n
            self._tpb = (8, 8, 8)
            _bpg = []
            for i in range(3):
                _bpg.append((shape[i] + (self._tpb[i] - 1)) // self._tpb[i])

            self._bpg = tuple(_bpg)

        m = state.m

        H_slicing(
            state.m.tensor, state.material.A.tensor, self._h.tensor, state.mesh.dx
        )

        # compute lumped mass
        V = state.mesh.cell_volume
        Ms = state.material.Ms.tensor
        mass = Function(state).tensor
        mass[:-1, :-1, :-1] += V / 8.0 * Ms
        mass[:-1, :-1, 1:] += V / 8.0 * Ms
        mass[:-1, 1:, :-1] += V / 8.0 * Ms
        mass[:-1, 1:, 1:] += V / 8.0 * Ms
        mass[1:, :-1, :-1] += V / 8.0 * Ms
        mass[1:, :-1, 1:] += V / 8.0 * Ms
        mass[1:, 1:, :-1] += V / 8.0 * Ms
        mass[1:, 1:, 1:] += V / 8.0 * Ms

        self._h.tensor.multiply_(-1.0 / (constants.mu_0 * mass.unsqueeze(-1)))

        return self._h


